    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) ViewJPanelTable.getModel();
        model.setRowCount(0);
        
        for (Employee vs : history.getHistory())
        {
          Object[] row = new Object[10];
          row[0] = vs;
          row[1] = vs.getEmpId();
          row[2] = vs.getEmpAge();
          row[3] = vs.getEmpGender();
          row[4] = vs.getEmpStartDate();
          row[5] = vs.getEmpContactNumber();
          row[6] = vs.getEmpId();
          row[7] = vs.getEmpLevel();
          row[8] = vs.getEmpPositionTitle();
          row[9] = vs.getEmpTeamInfo();
          
          model.addRow(row);
          
          
          
        }
        
    }

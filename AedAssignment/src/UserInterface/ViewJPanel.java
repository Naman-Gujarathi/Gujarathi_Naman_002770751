    private void ViewJPanelDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewJPanelDeleteActionPerformed
       int selectedRowIndex = ViewJPanelTable.getSelectedRow();
      
       if(selectedRowIndex<0)
       {
         JOptionPane.showMessageDialog(this, "Please select a row to delete.");
         return;
       }
        DefaultTableModel model = (DefaultTableModel) ViewJPanelTable.getModel();
        Employee selectedEmployee = (Employee) model.getValueAt(selectedRowIndex, 0);
        
        history.deleteEmployee(selectedEmployee);
        JOptionPane.showMessageDialog(this, "Employee information deleted.");
        fillTable();
       
// TODO add your handling code here:
    }//GEN-LAST:event_ViewJPanelDeleteActionPerformed


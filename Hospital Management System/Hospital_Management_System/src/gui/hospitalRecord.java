    private void deleteHospitalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteHospitalButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow= viewHospitalTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from table.", 
                    "Error", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Hospital hospital=(Hospital) viewHospitalTable.getValueAt(selectedRow, 0);
        /*Ask confirmation*/
        int flag= JOptionPane.showConfirmDialog(this, "Are you sure want to remove?",
                "Warning", JOptionPane.YES_NO_OPTION);
        if(flag==0)
        {
            hospitalDirectory.DeleteHospital(hospital);
            populateHospitalTable(hospitalDirectory.getHospitalDirectory());
        }
    }//GEN-LAST:event_deleteHospitalButtonActionPerformed

    private void viewHospitalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewHospitalButtonActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = viewHospitalTable.getSelectedRow();
        
        if (selectedRowIndex<0) {
            JOptionPane.showMessageDialog(this,"There is no row present.");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) viewHospitalTable.getModel();
        Hospital selectedHospital = (Hospital) model.getValueAt(selectedRowIndex, 0);
        
        viewHospitalNameText.setText(selectedHospital.getHospitalName());
        viewHospitalAddressText.setText(selectedHospital.getHospitalAddress());
        viewHospitalCityText.setText(selectedHospital.getCityName());
        viewHospitalPinCodeText.setText(selectedHospital.getHospitalZipCode());
        viewHospitalTypeText.setText(selectedHospital.getType());
        
        
    }//GEN-LAST:event_viewHospitalButtonActionPerformed
     
    private void populateHospitalTable(ArrayList<Hospital>hospitalDirectory){
    DefaultTableModel model = (DefaultTableModel) viewHospitalTable.getModel();
    model.setRowCount(0);
    if(hospitalDirectory.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "No Hospital Added. Please Add Hospital"
                    , "Warning", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
    for(Hospital hospital : hospitalDirectory){
    Object[] row = new Object[5];
            row[0] = hospital;
            row[1]= hospital.getHospitalAddress();
            row[2]= hospital.getCityName();
            row[3]= hospital.getHospitalZipCode();
            row[4]= hospital.getHospitalZipCode();
            model.addRow(row);
    }
    }

    private void updateHospitalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateHospitalButtonActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = viewHospitalTable.getSelectedRow();
        
        if (selectedRowIndex<0) {
            JOptionPane.showMessageDialog(this,"Please Select A row to be Updated");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) viewHospitalTable.getModel();
        Hospital selectedHospital = (Hospital) model.getValueAt(selectedRowIndex, 0);
        
        model.setValueAt(viewHospitalNameText.getText(),selectedRowIndex,0);
        model.setValueAt(viewHospitalAddressText.getText(),selectedRowIndex,1);
        model.setValueAt(viewHospitalCityText.getText(),selectedRowIndex,2);
        model.setValueAt(viewHospitalPinCodeText.getText(),selectedRowIndex,3);
        model.setValueAt(viewHospitalTypeText.getText(),selectedRowIndex,4);
        
        String hospitalName = model.getValueAt(selectedRowIndex,0).toString();
        String hospitalAddress = model.getValueAt(selectedRowIndex,1).toString();
        String hospitalCity = model.getValueAt(selectedRowIndex,2).toString();
        String hospitalZipCode = model.getValueAt(selectedRowIndex,3).toString();
        String hospitalType = model.getValueAt(selectedRowIndex,4).toString();
        
        updateHospitalName(selectedHospital,hospitalName);
        updateHospitalAddress(selectedHospital,hospitalAddress);
        updateHospitalCity(selectedHospital,hospitalCity);
        updateHospitalZipCode(selectedHospital,hospitalZipCode);
        updateHospitalType(selectedHospital,hospitalType);
        
    }//GEN-LAST:event_updateHospitalButtonActionPerformed

